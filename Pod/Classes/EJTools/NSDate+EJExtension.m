+ (NSString*)ej_getWeekFromDate:(NSDate*)date
{
    if ([[NSDate date] isEqualToDate:date]) {
        return @"今天";
    }
    else if ([[NSDate dateWithTimeInterval:24 * 3600 sinceDate:[NSDate date]] isEqualToDate:date]) {
        return @"明天";
    }
    else {
         return [self ej_getWeekStrFromeDate:date];
    }
}

